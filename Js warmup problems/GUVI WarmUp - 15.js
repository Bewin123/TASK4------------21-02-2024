/* Given a word, "getLengthOfWord" returns the length of the given word. */

function getLengthOfWord(word) {
	return word.length;
}

// Example usage:
let word1 = "GUVI";
let lengthOfWord = getLengthOfWord(word1);
console.log(`The length of the word is: ${lengthOfWord}`);


