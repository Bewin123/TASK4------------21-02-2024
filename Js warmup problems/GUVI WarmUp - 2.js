/* Fill in your code that takes an number minutes and converts it to seconds. */

function toSeconds(min) {
	return min * 60;
}
// Example usage:
let minutesInput = 4;
let secondsOutput = toSeconds(minutesInput);
console.log(`${minutesInput} minutes is equal to ${secondsOutput} seconds.`);
